package com.johnhiott.sundayfunday.models;

public class Place {

  private String name;
  private String address;
  private double lat;
  private double lon;
  private String license;

  public String getName() {
    return name;
  }

  public String getAddress() {
    return address;
  }

  public double getLat() {
    return lat;
  }

  public double getLon() {
    return lon;
  }

  public String getLicense() {
    return license;
  }

}
